    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        
        String id = txtuserid.getText();
        String firstname = txtfirstname.getText();
        String lastname = txtlastname.getText();
        String username = txtusername.getText();
        String password = txtpassword.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");          
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pst = (PreparedStatement) con.prepareStatement("insert into user(id, firstname,lastname,username,password)values(?,?,?,?,?)");
        
           pst.setString(1,id);
           pst.setString(2,firstname);
           pst.setString(3,lastname);
           pst.setString(4,username);
           pst.setString(5,password);
           pst.executeUpdate();
           
           JOptionPane.showMessageDialog(null, "User Created");
           autoID();
        
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }//GEN-LAST:event_addButtonActionPerformed

        public void autoID(){
      
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select MAX(ID) from user");
            rs.next();
            rs.getString("MAX(id)");
            if(rs.getString("MAX(id)") == null){
                txtuserid.setText("US001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(id)").
                substring(2, rs.getString("MAX(id)").length()));
                
                id++;
                txtuserid.setText("US"+String.format("%03d", id));
            }                      
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }
        
